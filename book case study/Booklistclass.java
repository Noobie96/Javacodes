package com.example;

public class Booklistclass{
 int callno;
 int Book_Storeid;
 String Book_Name;
 String Author;
 String Publisher;
 int Quantity;
 double Price;
 
 
public int getCallno() {
	return callno;
}
public void setCallno(int callno) {
	this.callno = callno;
}
public int getBook_Storeid() {
	return Book_Storeid;
}
public void setBook_Storeid(int book_Storeid) {
	Book_Storeid = book_Storeid;
}
public String getBook_Name() {
	return Book_Name;
}
public void setBook_Name(String book_Name) {
	Book_Name = book_Name;
}
public String getAuthor() {
	return Author;
}
public void setAuthor(String author) {
	Author = author;
}
public String getPublisher() {
	return Publisher;
}
public void setPublisher(String publisher) {
	Publisher = publisher;
}
public int getQuantity() {
	return Quantity;
}
public void setQuantity(int quantity) {
	Quantity = quantity;
}
public double getPrice() {
	return Price;
}
public void setPrice(double price) {
	Price = price;
}


}
